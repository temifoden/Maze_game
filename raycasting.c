void render(SDL_Renderer* renderer) {
    // Draw ceiling
    SDL_SetRenderDrawColor(renderer, 135, 206, 235, 255); // Sky blue for ceiling
    SDL_Rect ceilingRect = {0, 0, SCREEN_WIDTH, SCREEN_HEIGHT / 2}; // Define rectangle for ceiling
    SDL_RenderFillRect(renderer, &ceilingRect); // Fill ceiling area

    // Draw floor
    SDL_SetRenderDrawColor(renderer, 34, 139, 34, 255); // Green for grass-like floor
    SDL_Rect floorRect = {0, SCREEN_HEIGHT / 2, SCREEN_WIDTH, SCREEN_HEIGHT / 2}; // Define rectangle for floor
    SDL_RenderFillRect(renderer, &floorRect); // Fill floor area


    int numRays = SCREEN_WIDTH; // Cast one ray for every vertical pixel column on the screen
    for (int x = 0; x < numRays; x++) {
        double cameraX = 2 * x / (double)numRays - 1;
        double rayDirX = dirX + planeX * cameraX;
        double rayDirY = dirY + planeY * cameraX;

        int mapX = (int)posX;
        int mapY = (int)posY;

        double sideDistX, sideDistY;

        double deltaDistX = fabs(1 / rayDirX);
        double deltaDistY = fabs(1 / rayDirY);
        double perpWallDist;

        int stepX, stepY;

        int hit = 0;
        int side;

        //  DDA algorithm to find wall hit
        if (rayDirX < 0) {
            stepX = -1;
            sideDistX = (posX - mapX) * deltaDistX;
        } else {
            stepX = 1;
            sideDistX = (mapX + 1.0 - posX) * deltaDistX;
        }
        if (rayDirY < 0) {
            stepY = -1;
            sideDistY = (posY - mapY) * deltaDistY;
        } else {
            stepY = 1;
            sideDistY = (mapY + 1.0 - posY) * deltaDistY;
        }

        while (hit == 0) {
            if (sideDistX < sideDistY) {
                sideDistX += deltaDistX;
                mapX += stepX;
                side = 0;
            } else {
                sideDistY += deltaDistY;
                mapY += stepY;
                side = 1;
            }
            if (map[mapX][mapY] > 0) hit = 1;
        }

        //  Determine where the wall and calculate distance to it
        if (side == 0)
            perpWallDist = (mapX - posX + (1 - stepX) / 2) / rayDirX;
        else
            perpWallDist = (mapY - posY + (1 - stepY) / 2) / rayDirY;
        
        //  calculate height of the line to draw
        int lineHeight = (int)(SCREEN_HEIGHT / perpWallDist);

        int drawStart = -lineHeight / 2 + SCREEN_HEIGHT / 2;
        if (drawStart < 0) drawStart = 0;
        int drawEnd = lineHeight / 2 + SCREEN_HEIGHT / 2;
        if (drawEnd >= SCREEN_HEIGHT) drawEnd = SCREEN_HEIGHT - 1;
        
        //Set color for the wall
        if (side == 1) {
            //  Dark red for side
            SDL_SetRenderDrawColor(renderer, 128, 0, 0, 255);
        } else{
            //  Bright red for front
            SDL_SetRenderDrawColor(renderer, 255, 0, 0, 255);
        }
        
        // Draw the wall
        SDL_RenderDrawLine(renderer, x, drawStart, x, drawEnd);
    }

}
